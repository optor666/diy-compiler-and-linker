/* multiplication_table.c源文件 */
/*********************************************************** 
 * 功能:	全局变量测试
 **********************************************************/
void multiplication_table()
{
	int i,j;
	for(i = 1; i <= 9; i = i+1)
	{
		for(j=1; j <= i; j= j+1)
		{
			printf("%d*%d=%d  ",j,i,j*i);
		}
		printf("\n");
	}
}

/*********************************************************** 
 * 功能:	main函数
 **********************************************************/
int  main()
{
	multiplication_table();	
	return 0;
}

/*********************************************************** 
 * 功能:	入口函数
 **********************************************************/
void  _entry()
{
	int ret;
	ret = main();
	exit(ret);
}